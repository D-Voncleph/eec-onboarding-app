-- 1. Create a table for public profiles
create table if not exists profiles (
  id uuid references auth.users not null primary key,
  email text,
  full_name text,
  updated_at timestamp with time zone
);

-- 2. Enable RLS on profiles
alter table profiles enable row level security;

create policy "Public profiles are viewable by everyone." on profiles
  for select using (true);

create policy "Users can insert their own profile." on profiles
  for insert with check (auth.uid() = id);

create policy "Users can update own profile." on profiles
  for update using (auth.uid() = id);

-- 3. Sequences Table
create table if not exists sequences (
  id bigint generated by default as identity primary key,
  active boolean default false,
  content jsonb default '[]'::jsonb,
  updated_at timestamp with time zone default timezone('utc'::text, now())
);

-- Add user_id column if it doesn't exist
do $$ 
begin 
  if not exists (select 1 from information_schema.columns where table_name = 'sequences' and column_name = 'user_id') then
    alter table sequences add column user_id uuid references auth.users;
  end if; 
end $$;

-- Enable RLS
alter table sequences enable row level security;

-- Drop existing policies to avoid conflicts
drop policy if exists "Users can view own sequences" on sequences;
drop policy if exists "Users can update own sequences" on sequences;
drop policy if exists "Users can insert own sequences" on sequences;

-- Create Policies
create policy "Users can view own sequences" on sequences
  for select using (auth.uid() = user_id);

create policy "Users can update own sequences" on sequences
  for update using (auth.uid() = user_id);

create policy "Users can insert own sequences" on sequences
  for insert with check (auth.uid() = user_id);

