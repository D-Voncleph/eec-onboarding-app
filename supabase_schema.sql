-- 5. Trigger to create profile on signup
create or replace function public.handle_new_user() 
returns trigger as $$
begin
  insert into public.profiles (id, email, full_name)
  values (new.id, new.email, new.raw_user_meta_data->>'full_name');
  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

-- 6. Member Events Table (for tracking metrics)
create table if not exists member_events (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  member_email text not null,
  event_type text not null, -- 'sequence_started', 'email_sent', 'sequence_completed', 'refund'
  event_data jsonb default '{}'::jsonb,
  created_at timestamp with time zone default timezone('utc'::text, now())
);

-- Add index for faster queries
create index if not exists idx_member_events_user_id on member_events(user_id);
create index if not exists idx_member_events_type on member_events(user_id, event_type);

-- Enable RLS
alter table member_events enable row level security;

-- Drop existing policies
drop policy if exists "Users can view own events" on member_events;
drop policy if exists "Users can insert own events" on member_events;

-- Create Policies
create policy "Users can view own events" on member_events
  for select using (auth.uid() = user_id);

create policy "Users can insert own events" on member_events
  for insert with check (auth.uid() = user_id);

